#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*t;

	t = (unsigned char*)s;
	if (n != 0)
	{
		i = 0;
		while (i < n)
		{
			t[i++] = '\0';
		}
	}
}
